// Теория2. Стэк. Это самая простая задача на стэк, почитай в интернете или спроси у чат-бота, обсудим:
// Напиши функцию, которая принимает строку, содержащую только символы '(', ')', '{', '}', '[' и ']', 
// и проверяет, является ли последовательность скобок корректной.
function isValid(s) {
    // Создаем пустой массив для стэка
    const stack = [];
    
    // Создаем объект, в котором хранится соответствие открывающих и закрывающих скобок
    const pairs = {
        '(': ')',
        '{': '}',
        '[': ']'
    };
    
    // Проходим по каждому символу строки
    for (let i = 0; i < s.length; i++) {
        let char = s[i]; // Берем текущий символ

        // Если это открывающая скобка, добавляем её в стэк
        if (pairs[char]) {
            stack.push(char); // Добавляем в стэк
        } else {
            // Если это закрывающая скобка, нужно проверить пару
            let last = stack.pop(); // Удаляем последнюю открытую скобку из стэка
            if (pairs[last] !== char) {
                // Если она не соответствует текущей закрывающей скобке
                return false;
            }
        }
    }

    // Если после обработки всех символов стэк пустой, последовательность корректная
    return stack.length === 0;
}

// Пример использования
console.log(isValid("(){}[]"));    // true
console.log(isValid("({[)]}"));    // false
console.log(isValid("{[()]}"));    // true

isValid("(){}[]");    // true
isValid("({[)]}");    // false
isValid("{[()]}");    // true

// Скобочки могут быть вложенными, но вложенность должна быть правильной.